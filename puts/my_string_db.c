t_draw_map	get_letter(const char letter)
{
	const t_draw_map	empty = {letter, {{-1, -1}}};

	if (letter >= 'A' && letter <= 'M')
		return (get_first_maiusc_letter(letter));
	else if (letter >= 'N' && letter <= 'Z')
		return (get_second_maiusc_letter(letter));
	return (empty);
}
